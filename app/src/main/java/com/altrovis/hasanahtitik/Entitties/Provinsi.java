public class Provinsi {

    private int ID;
    private String Nama;
    private int NegaraID;

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getNama() {
        return Nama;
    }

    public void setNama(String nama) {
        Nama = nama;
    }

    public int getNegaraID() {
        return NegaraID;
    }

    public void setNegaraID(int negaraID) {
        NegaraID = negaraID;
    }
}
